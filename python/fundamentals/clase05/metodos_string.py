curso = "pYtHon bAsic"
print(curso.upper())
print(curso.lower())
print(curso.capitalize()) #Primera letra en mayuscula de cada palabra
print(curso.title()) #Primera letra en mayuscula de todo el texto
print(curso.swapcase()) #Cambia mayusculas por minusculas y viceversa
print(curso.strip()) #Elimina espacios en blanco
print(curso.lstrip()) #Elimina espacios en blanco a la izquierda
print(curso.rstrip()) #Elimina espacios en blanco a la derecha
print(curso.find("o")) #Busca la primera ocurrencia
print(curso.center(20, "*")) #Agrega asteriscos al principio y al final centrado el texto
print(".".join(curso)) #Separa cada letra con un punto